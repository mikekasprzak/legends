// - ------------------------------------------------------------------------------------------ - //
// Assert //
// - ------------------------------------------------------------------------------------------ - //
#ifndef __GEL_DEBUG_Assert_H__
#define __GEL_DEBUG_Assert_H__
// - ------------------------------------------------------------------------------------------ - //
#include <stdlib.h>
#include "Log.h"
// - ------------------------------------------------------------------------------------------ - //
#ifndef NDEBUG
// - ------------------------------------------------------------------------------------------ - //
// Assertion //
#define Assert( ___TEST, ... ) \
	{if ( ___TEST ) { \
		LogAlways( "-========================================================-" ); \
		_LogAlways( "! Assert Error: " ); \
		LogAlways( __VA_ARGS__ ); \
		LogAlways( "   Line: %i  File: %s", __LINE__, __FILE__ ); \
		LogAlways( "   Func: %s", __PRETTY_FUNCTION__ ); \
		LogAlways( "-========================================================-" ); \
		::exit(1); \
	}}
// - ------------------------------------------------------------------------------------------ - //
// Warnings //
#define Warning( ___TEST, ... ) \
	{if ( ___TEST ) { \
		LogAlways( "-========================================================-" ); \
		_LogAlways( "! Warning: " ); \
		LogAlways( __VA_ARGS__ ); \
		LogAlways( "   Line: %i  File: %s", __LINE__, __FILE__ ); \
		LogAlways( "   Func: %s\n", __PRETTY_FUNCTION__ ); \
	}}
// - ------------------------------------------------------------------------------------------ - //
// Warnings (return) //
#define return_Warning( ___TEST, ... ) \
	{if ( ___TEST ) { \
		LogAlways( "-========================================================-" ); \
		_LogAlways( "! Warning: " ); \
		LogAlways( __VA_ARGS__ ); \
		LogAlways( "   Line: %i  File: %s", __LINE__, __FILE__ ); \
		LogAlways( "   Func: %s\n", __PRETTY_FUNCTION__ ); \
		return; \
	}}
// - ------------------------------------------------------------------------------------------ - //
// Warnings (return value) //
#define return_value_Warning( ___VAL, ___TEST, ... ) \
	{if ( ___TEST ) { \
		LogAlways( "-========================================================-" ); \
		_LogAlways( "! Warning: " ); \
		LogAlways( __VA_ARGS__ ); \
		LogAlways( "   Line: %i  File: %s", __LINE__, __FILE__ ); \
		LogAlways( "   Func: %s\n", __PRETTY_FUNCTION__ ); \
		return ___VAL; \
	}}
// - ------------------------------------------------------------------------------------------ - //
#else // NDEBUG //
// - ------------------------------------------------------------------------------------------ - //
#define Assert( ... ) ;
#define Warning( ... ) ;
#define return_Warning( ... ) ;
#define return_value_Warning( ... ) ;
// - ------------------------------------------------------------------------------------------ - //
#endif // NDEBUG //
// - ------------------------------------------------------------------------------------------ - //
#endif // __GEL_DEBUG_Assert_H__ //
// - ------------------------------------------------------------------------------------------ - //
