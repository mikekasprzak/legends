// - ------------------------------------------------------------------------------------------ - //
#include <GEL.h>
#include "GameSupport.h"
// - ------------------------------------------------------------------------------------------ - //
cGameSupport* cGameSupport::CurrentInstance = 0;
// - ------------------------------------------------------------------------------------------ - //

// - ------------------------------------------------------------------------------------------ - //
cGameSupport::cGameSupport() {
	CurrentInstance = this;
	CurrentAngle = 0;

	// Initialize Subsystems //
	MouseInit();
	FadeInit();
	LostFocusInit();
}
// - ------------------------------------------------------------------------------------------ - //
cGameSupport::~cGameSupport() {
	LostFocusExit();
	FadeExit();
	MouseExit();
}
// - ------------------------------------------------------------------------------------------ - //


// - ------------------------------------------------------------------------------------------ - //
void cGameSupport::Step() {
	MouseStep();
	FadeStep();
	if ( LostFocus ) {
		LostFocusStep();
	}
}
// - ------------------------------------------------------------------------------------------ - //
void cGameSupport::Draw() {
	MouseDraw();
	FadeDraw();
	if ( LostFocus ) {
		LostFocusDraw();
	}
}
// - ------------------------------------------------------------------------------------------ - //

