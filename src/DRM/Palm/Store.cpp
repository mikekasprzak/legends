// - ------------------------------------------------------------------------------------------ - //
bool gelStoreAuthorize() {
	Log( "- Initializing Palm DRM\n", TestDate );

	bool Authorized = true;

	if ( WebOS_PDL_isAppLicensedForDevice(PALM_APPID) == 0 )
	{
		Notice( "Authorization Failed (%s)", PALM_APPID );
		Authorized = false;
	}
	Log("\n");
	
	return Authorized;
}
